public class StringExercise {

    public static void main(String[] args) {

        String brickInTheWall = "We don't need no education \nWe don't need no thought control";
        System.out.println(brickInTheWall);

        String quotes = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotes);

        String drive = "In windows, the main drive is usually C:\\";
        System.out.println(drive);

        String backslash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.print(backslash);
    }

}
